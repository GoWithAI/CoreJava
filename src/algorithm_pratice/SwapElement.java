package algorithm_pratice;

public class SwapElement {

	public static void main(String[] args) {

		int a = 3; // space is 1 unit
		int b = 2; // space is 1 unit
		int temp = 5; // space is 1 unit

		// space function f(3)=3
		// space complexity O(1)

		// swap elements
		temp = a; // Time taken is 1 unit 1
		// for algebraic it take more
		a = b; // TIme taken is 1 unit
		b = temp; // Time Taken is 1 unit
		// Time Function f(3)=3
		// Time complexity O(1)

	}

}
